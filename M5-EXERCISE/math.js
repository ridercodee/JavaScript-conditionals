// Addition
let val1 = 2+3;
console.log(`val1: ${val1}`);

// Substraction
let val2 = 3-1;
console.log(`val2: ${val2}`);

// Division
let val3 = 3/1;
console.log(`val3: ${val3}`);
let val4 = 21 / 9;
console.log(`val4: ${val4}`);

// Multiplication
let val5 = 3*6;
console.log(`val5: ${val5}`);

// Increment and Decrement
let val6 = 2;
++val6;
console.log(`val6: ${val6}`);

let val7 = 1;
--val7;
console.log(`val7: ${val7}`);